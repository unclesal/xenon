// *********************************************************************************************************************
// *                                                                                                                   *
// *                                                    Конструктор                                                    *
// *                                                                                                                   *
// *********************************************************************************************************************

StateFrame::StateFrame(BimboAircraft * bimbo, ConnectedCommunicatorReactor * environment ) 
    : AbstractFrame()
{
    _environment = environment;
    _ptr_acf = bimbo;
    
    _action = ACF_DOES_NOTHING;
    _likeliness = 0;
    
};

// *********************************************************************************************************************
// *                                                                                                                   *
// *                                                Результат фрейма                                                   *
// *                                                                                                                   *
// *********************************************************************************************************************

void StateFrame::result( next_action_t & next_action ) {
    next_action.action = _action;
    next_action.likeliness = _likeliness;
}
